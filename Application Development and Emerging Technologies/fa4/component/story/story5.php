<h2>Harry Potter and the Deathly Hallows – Part 2</h2>
<p>
    Harry, Ron, and Hermione continue searching for Voldemort’s remaining Horcruxes while preparing for the final
    battle. They return to Hogwarts, where students and teachers unite to defend the school against Voldemort and his
    followers. The castle soon becomes the center of a massive magical war.
</p>
<br>
<p>
    As the battle continues, Harry discovers that he himself contains a piece of Voldemort’s soul. He willingly
    sacrifices himself in order to destroy the final Horcrux and give everyone a chance to defeat Voldemort. Meanwhile,
    Neville Longbottom destroys Voldemort’s snake, Nagini, weakening the dark wizard even further.
</p>
<br>
<p>
    Harry eventually confronts Voldemort in a final duel inside Hogwarts. With all the Horcruxes destroyed, Voldemort
    is finally defeated, bringing peace back to the wizarding world. The story ends with an epilogue showing Harry,
    Ron, and Hermione years later as adults with families of their own.
</p>