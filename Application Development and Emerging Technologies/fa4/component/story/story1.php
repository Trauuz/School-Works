<h2>Harry Potter and the Sorcerer's Stone</h2>
<p>
    Harry Potter learns that he is a wizard after living a difficult life with his aunt and uncle. He receives an
    invitation to attend Hogwarts School of Witchcraft and Wizardry. There, he meets Ron Weasley and Hermione Granger,
    who become his closest friends. Harry also discovers that he is famous in the wizarding world for surviving an
    attack by the dark wizard Voldemort.
</p>
<br>
<p>
    As Harry adjusts to life at Hogwarts, he experiences magical classes, flying lessons, and the mystery surrounding a
    powerful object called the Sorcerer’s Stone. Strange events around the school lead Harry and his friends to suspect
    that someone is trying to steal the stone. They face dangerous challenges while searching for the truth. Along the
    way, Harry learns more about courage and friendship.
</p>
<br>
<p>
    In the end, Harry confronts Voldemort, who is trying to return to power. With the help of his friends and the
    protection of his mother's love, Harry manages to prevent Voldemort from obtaining the Sorcerer’s Stone. The story
    ends with Harry returning to the Dursleys for the summer, looking forward to his next year at Hogwarts.
</p>