<h2>Harry Potter and the Prisoner of Azkaban</h2>
<p>
    Harry learns that Sirius Black, a dangerous prisoner from Azkaban, has escaped and may be searching for him.
    Dementors are placed around Hogwarts to protect the students, but they greatly affect Harry. During the school
    year, Harry becomes closer to Professor Lupin, who teaches him how to defend himself against the Dementors.
</p>
<br>
<p>
    As Harry uncovers secrets about his parents’ past, he discovers that Sirius Black was actually a close friend of
    his family. The true betrayer is revealed to be Peter Pettigrew, who has been hiding for years. Harry, Ron, and
    Hermione become caught in a dangerous situation involving both Sirius and Pettigrew.
</p>
<br>
<p>
    Using Hermione’s Time-Turner, Harry and Hermione travel back in time to rescue Sirius and Buckbeak the hippogriff.
    Harry also performs a powerful Patronus Charm to save himself and Sirius from the Dementors. Although Sirius must
    remain in hiding, Harry gains hope after learning he still has family who care about him.
</p>