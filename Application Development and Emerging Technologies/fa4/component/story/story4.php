<h2>Harry Potter and the Goblet of Fire</h2>
<p>
    Hogwarts hosts the dangerous Triwizard Tournament, where students from different wizarding schools compete in
    magical challenges. Although Harry is too young to enter, his name mysteriously appears in the Goblet of Fire,
    forcing him to participate. Many students believe Harry cheated to join the competition.
</p>
<br>
<p>
    Harry faces several deadly tasks, including battling dragons and surviving underwater challenges. While trying to
    understand who entered him into the tournament, Harry receives help from his friends and teachers. During the final
    challenge, Harry and Cedric Diggory are transported to a graveyard through a magical trap.
</p>
<br>
<p>
    In the graveyard, Voldemort returns to physical form using dark magic. Cedric is killed, and Harry barely escapes
    after a dangerous duel with Voldemort. Harry returns to Hogwarts and warns everyone that Voldemort has returned,
    marking the beginning of a darker and more dangerous time.
</p>