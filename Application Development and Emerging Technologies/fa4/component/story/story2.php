<h2>Harry Potter and the Chamber of Secrets</h2>
<p>
    Harry Potter returns to Hogwarts for his second year and soon discovers that something dangerous is happening inside
    the school. Students are mysteriously being turned to stone, and messages appear warning that the Chamber of Secrets
    has been opened. Harry, Ron, and Hermione begin investigating the mystery together.
</p>
<br>
<p>
    As the attacks continue, Harry learns more about the history of Hogwarts and the legend of the chamber. Many
    students become suspicious of Harry because he can speak Parseltongue, the language of snakes. Hermione discovers
    that the creature inside the chamber is a giant basilisk controlled through dark magic.
</p>
<br>
<p>
    Harry eventually enters the hidden chamber to save Ginny Weasley, who has been trapped by Tom Riddle. With the help
    of Fawkes the phoenix and the Sword of Gryffindor, Harry defeats the basilisk and destroys Riddle’s diary. The
    attacks finally stop, and peace returns to Hogwarts once again.
</p>